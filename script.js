const getStoredLanguage = () => {
  try {
    const storedLanguage = window.localStorage.getItem("preferred-language");
    return storedLanguage && translations[storedLanguage] ? storedLanguage : "en";
  } catch {
    return "en";
  }
};

const storeLanguage = (languageCode) => {
  try {
    window.localStorage.setItem("preferred-language", languageCode);
  } catch {
    // Ignore storage failures in restricted environments.
  }
};

const buildLinkAriaLabel = (languageCode, label) => {
  if (languageCode === "ar") {
    return `افتح ${label}`;
  }

  if (languageCode === "ckb") {
    return `${label} بکەرەوە`;
  }

  if (languageCode === "zh") {
    return `打开${label}`;
  }

  return `Open ${label}`;
};

const renderLinks = (languageCode) => {
  if (!linksGrid) {
    return;
  }

  const localizedLinks = translations[languageCode].links;
  linksGrid.innerHTML = "";

  profileLinks.forEach((link) => {
    const localizedLink = localizedLinks[link.key];
    const card = document.createElement(link.href ? "a" : "article");

    card.className = "link-card link-card-simple reveal is-visible";
    card.dataset.wide = String(Boolean(link.wide));
    card.dataset.theme = link.theme;

    if (link.href) {
      card.href = link.href;
      card.setAttribute("aria-label", buildLinkAriaLabel(languageCode, localizedLink.label));

      if (link.href.startsWith("http")) {
        card.target = "_blank";
        card.rel = "noreferrer";
      }
    }

    card.innerHTML = `
      <div class="link-topline">
        <span class="link-mark">
          <span class="link-mark-inner">${link.icon}</span>
        </span>
        <span class="link-action" aria-hidden="true">&#8599;</span>
      </div>
      <div class="link-main">
        <p class="link-label">${localizedLink.label}</p>
        <p class="link-value">${localizedLink.value}</p>
      </div>
      <div class="link-bottomline">
        <span class="link-line" aria-hidden="true"></span>
        <span class="link-glow-dot" aria-hidden="true"></span>
      </div>
    `;

    linksGrid.appendChild(card);
  });
};

